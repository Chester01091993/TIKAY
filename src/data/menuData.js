// Función para obtener todos los productos con imágenes
export const getProductsWithImages = () => {
  return menuData.flatMap(category => 
    category.items.filter(item => item.image)
  );
};

// Función para obtener productos por categoría
export const getProductsByCategory = (categoryId) => {
  const category = menuData.find(cat => cat.id === categoryId);
  return category ? category.items.filter(item => item.image) : [];
};

// Función para buscar productos
export const searchProducts = (searchTerm) => {
  const allProducts = menuData.flatMap(category => category.items);
  return allProducts.filter(item => 
    item.name.toLowerCase().includes(searchTerm.toLowerCase()) && item.image
  );
}; 